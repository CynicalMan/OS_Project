package GradingSystem;

public class Subject {
    private int id;
    private String name;
    private int creditHours;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getCreditHours() {
        return creditHours;
    }

    public void setCreditHours(int creditHours) {
        this.creditHours = creditHours;
    }

}
